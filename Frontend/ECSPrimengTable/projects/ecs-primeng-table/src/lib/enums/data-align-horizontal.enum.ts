/**
 * Defines the horizontal alignment of content within a table cell.
 *
 * @remarks
 * Specifies how the content inside a cell is positioned horizontally relative to the cell's width.
 */
export enum DataAlignHorizontal {
    /** Content is aligned to the left side of the cell. */
    Left,

    /** Content is aligned to the center of the cell. */
    Center,

    /** Content is aligned to the right side of the cell. */
    Right
}